/**
 * Enum representing different types of work locations.
 */
public enum LocationType {
    /**
     * On-site work location.
     */
    ON_SITE,
    /**
     * Remote work location.
     */
    REMOTE,
    /**
     * Hybrid work location (mix of on-site and remote).
     */
    HYBRID
}
